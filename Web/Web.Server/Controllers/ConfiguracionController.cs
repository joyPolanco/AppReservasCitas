using Aplicacion.DTOs;
using Aplicacion.DTOs.Planificacion;
using Aplicacion.Interfaces;
using Dominio.Entidades;
using Dominio.Entidades.Models;
using Infraestructura.Persistencia.Loggers;
using Microsoft.AspNetCore.Authorization;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Mvc;

namespace Web.Server.Controllers
{
    [Route("api/[controller]")]
    [ApiController]
    public class ConfiguracionController : ControllerBase
    {
        private IConfiguracionService _service;

        public ConfiguracionController(IConfiguracionService configuracionService)
        {
            _service = configuracionService;
             
        }
        [HttpPost]
        public async Task<IActionResult> AplicarConfiguracion([FromBody] AppConfiguracionDTO config)
        {
            var logger = LoggerContext.GetLogger; 
            try
            {
                await _service.Actualizar(config);


                LoggerContext.GetLogger().SetDatabaseLogger(); 
                await LoggerContext.GetLogger().Registrar(new Log()
                {
                    FechaHora = DateTime.UtcNow,
                    Tipo = "Modificacion",
                    Mensaje = "La configuración fue cambiada"
                });

                return Ok(new { message = "Configuración actualizada correctamente" });
            }
            catch (ArgumentException e)
            {

                LoggerContext.GetLogger().SetFileLogger(); //
                await LoggerContext.GetLogger().Registrar(new Log()
                {
                    FechaHora = DateTime.UtcNow,
                    Tipo = "Error",
                    Mensaje = e.Message
                });

                return BadRequest(new { error = e.Message });
            }
            catch (Exception ex)
            {

                LoggerContext.GetLogger().SetFileLogger(); //
                await LoggerContext.GetLogger().Registrar(new Log()
                {
                    FechaHora = DateTime.UtcNow,
                    Tipo = "Error",
                    Mensaje = ex.Message
                });

                return StatusCode(500, new { error = "Ocurrió un error interno al aplicar la configuración" });
            }
        }
        [HttpGet("turnos")]
        public async Task<ActionResult<List<TurnoDTO>>> GetTurnos()
        {
            var logger = LoggerContext.GetLogger; // Singleton
            try
            {
                var turnos = await _service.GetTurnoDTOs();
                if (turnos == null)
                {
                    return NotFound("No se encontraron turnos disponibles");
                }
                return Ok(turnos);
            }
            catch (Exception ex)
            {
                // Registrar error en archivo
                LoggerContext.GetLogger().SetFileLogger(); //
                await LoggerContext.GetLogger().Registrar(new Log
                {
                    FechaHora = DateTime.UtcNow,
                    Tipo = "Error",
                    Mensaje = ex.Message
                });

                return StatusCode(500, $"Ocurrió un error al obtener los turnos: {ex.Message}");
            }
        }
        [HttpGet]
        public async Task<ActionResult<AppConfiguracionDTO>> GetConfiguracion()
        {
            var logger = LoggerContext.GetLogger;
            try
            {
                var config = await _service.Get();
                if (config == null)
                {
                    return NotFound("No se encontró ninguna configuración");
                }
                return Ok(config);
            }
            catch (Exception ex)
            {
                // Registrar error en archivo
                LoggerContext.GetLogger().SetFileLogger(); //
                await LoggerContext.GetLogger().Registrar(new Log
                {
                    FechaHora = DateTime.UtcNow,
                    Tipo = "Error",
                    Mensaje = ex.Message
                });

                return StatusCode(500, $"Ocurrió un error al obtener la configuración: {ex.Message}");
            }
